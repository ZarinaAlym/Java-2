package operators;

public class Comparison {
    public static void main(String[] args) {
        System.out.println(89 > 34);
        System.out.println(12 < 49);

        System.out.println(15 == 15); // true
        System.out.println(15 != 15); // false

        System.out.println(40 >= 20); // true
        System.out.println(12 >= 45);  // false
        System.out.println(1 <= 2);  // true


    }
}
