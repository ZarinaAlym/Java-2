package operators;

public class Assingment {
    public static void main(String[] args) {

        // = += -= *= /= %= &= |= ^=
        // =
        int num = 10;
        System.out.println(num);

       // +=
        num += 5;
        // num= num + 5
        System.out.println(num);

        // -=
        num -=1;
        System.out.println(num);

        //*=
        num *=2;
        System.out.println(num);

        // /=
        num /= 4;
        System.out.println(num);

        // %=
        num %= 3;
        System.out.println(num);


    }
}